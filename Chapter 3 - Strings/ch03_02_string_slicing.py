greeting = "Good Morning, "
name = "Abdullah"
# Concatenating two strings
print(greeting + name)

# Printing character of string
# Starts from 0
print(name[0])

# Printing part of string
print(name[0:3])
print(name[:3])
# Prints Abd, position 0, 1, and 2

# Printing full name
print(name[0:])
print(name[0:8])

# Printing using index
print(name[-4:-1])
print(name[4:7])

# Printing using jumps
# This jumps 2
print(name[0::2])
print(name[0::3])