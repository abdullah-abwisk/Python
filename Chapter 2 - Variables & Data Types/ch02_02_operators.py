a = 3
b = 4

# Arithmetic Operators
print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b)

# Assignment Operators
a += 12
a -= 12
a *= 12
a /= 12
print(a)

# Comparison Operators
b = (14>7)
print(b)

# Logical Operators
bool1 = True
bool2 = False
print("Bool1 and Bool2 = ", (bool1 and bool2))
print("Bool1 and Bool2 = ", (bool1 or bool2))
print("Not Bool2 = ", (not bool2))